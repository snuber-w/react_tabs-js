export const Tabs = ({ tabs, activeTabId, onTabSelected }) => {
  let activeTabIdChecked = activeTabId;

  if (tabs.findIndex(tab => tab.id === activeTabId) === -1) {
    activeTabIdChecked = tabs[0].id;
  }

  return (
    <div data-cy="TabsComponent">
      <div className="tabs is-boxed">
        <ul>
          {tabs.map(tab => (
            <li
              key={tab.id}
              className={tab.id === activeTabIdChecked ? 'is-active' : ''}
              data-cy="Tab"
            >
              <a
                href={`#${tab.id}`}
                data-cy="TabLink"
                onClick={() => {
                  if (tab.id !== activeTabIdChecked) {
                    onTabSelected(tab.id);
                  }
                }}
              >
                {tab.title}
              </a>
            </li>
          ))}
        </ul>
      </div>

      <div className="block" data-cy="TabContent">
        {tabs.find(tab => tab.id === activeTabIdChecked).content}
      </div>
    </div>
  );
};
